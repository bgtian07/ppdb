<script>
    var navLinks = document.getElementById("navLinks");
    function showMenu() {
        navLinks.style.right = "0";
    }
    function hideMenu() {
        navLinks.style.right = "-200px";
    }
    
    // Counter Function with AOS
    const counters = document.querySelectorAll('[data-count]');
    const speed = 200; // kecepatan penghitung

    // Fungsi untuk menjalankan counter saat muncul di layar
    function startCounters() {
        counters.forEach(counter => {
            const target = +counter.getAttribute('data-count');
            const count = +counter.innerText;

            const increment = target / speed;

            if(count < target) {
                counter.innerText = Math.ceil(count + increment);
                setTimeout(startCounters, 1);
            } else {
                counter.innerText = target;
            }
        });
    }

    // Inisialisasi AOS
    AOS.init({
        offset: 120,  // Jarak sebelum animasi muncul
        once: true,   // Animasi hanya terjadi sekali
    });

    // Menambahkan event listener pada AOS untuk menjalankan counter saat section muncul
    AOS.refresh();
    document.addEventListener('aos:in', startCounters); // Memulai counter saat elemen terdeteksi muncul

</script>

